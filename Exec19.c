#include <stdio.h>

int main(){



    //declaraçao de variaveis
    int area,comprimento,largura;
    //entrada de dados
    printf("Digite a largura do lote: ");
    scanf("%d",&largura);
    printf("Digite o comprimento do lote: ");
    scanf("%d",&comprimento);
    //processamento de dados
    area = comprimento * largura;
    //saida de dados
    printf("Area do lote: %d\n",area);







    return 0;
}

